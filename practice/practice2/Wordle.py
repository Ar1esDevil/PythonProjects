def wordle_feedback(secret, guess):
    feedback = []
    for i in range(5):
        if guess[i] == secret[i]:
            feedback.append(f"[{guess[i]}]")  
        elif guess[i] in secret:
            feedback.append(f"({guess[i]})")  
        else:
            feedback.append(guess[i])  
    return ''.join(feedback)

def wordle():
    secret_word = generate_word()
    attempts = 0

    print("Добро пожаловать в игру 'Wordle'!")
    print("Попробуйте угадать 5-буквенное слово.")

    while True:
        guess = input("Введите ваше предположение (5 букв): ").lower()
        if len(guess) != 5 or not guess.isalpha():
            print("Неверный ввод. Пожалуйста, введите 5-буквенное слово.")
            continue

        attempts += 1
        feedback = wordle_feedback(secret_word, guess)

        print(f"Результат: {feedback}")

        if guess == secret_word:
            print(f"Поздравляем! Вы угадали слово '{secret_word}' за {attempts} попыток.")
            break

if __name__ == "__main__":
    wordle()